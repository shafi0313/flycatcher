    {
        $data = [
            'parcel_id' => $request->parcel_id,
            'note' => $request->note,
            'admin_id' => auth('admin')->user()->id,
            'guard_name' => 'admin'
        ];
       ParcelNote::create($data);
       return response()->successRedirect('Note Added !', '');
    }

